"""
Tipo None
- O Tipo de dado None em Python representa o tipo sem tipo, ou poderia ser conhecido também
como tipo vazio, porém, falar que é um tipo sem tipo é mais apropiado.

OBS: O tipo None é SEMPRE espefificado com a primeira letra Maiúnscula.
Certo = None
errado: none

Quando utilizar ?
    - Podemos utilizar None quando queremos criar uma variavel e inicializa-la com um tipo sem tipo,
    antes de receber um valor final

OBS: o tipo None em Python é sempre considerado como False
"""
numeros = None
print(numeros)
print(type(numeros))

numeros = 1.33, 1.23, 5.34
print(numeros)
print(type(numeros))